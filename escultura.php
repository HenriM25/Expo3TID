             <h1>História da Escultura</h1>
        <p>A origem da Escultura tem inicio na Idade Paleolítica. Nessa Era as esculturas eram feitas a partir de materiais como marfim e ossos, e na maioria das vezes, essas esculturas formavam figuras femininas.</p>

        <p>As esculturas quase não estão presentes durante a Idade Mesolítica e na Idade Neolítica, mas apesar delas existirem em um número reduzido, houve um aprimoramento na técnica de produzi-las, através do conhecimento de lascar e polir a pedra. A pintura e a escultura são as primeiras manifestações artísticas e, ao longo do século, se relacionam com uma série de simbolismos.</p>
             <h1>Escultura no Brasil</h1>
