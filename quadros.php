    <h1>O que é arte figurativa?</h1>
<p>Ao contrario da arte abstrata, a arte figurativa ou figurativismo, é um estilo artístico das artes visuais pautado na representação das formas, seja de seres humanos, objetos, animais, paisagens, entre outros. A arte figurativa remonta séculos de existência humana. As pinturas indicavam a vontade do homem de expressar emoções, sentimentos e de copiar e reproduzir figuras da natureza.</p>
